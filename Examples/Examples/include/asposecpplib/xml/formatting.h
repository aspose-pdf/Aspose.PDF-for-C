/// @file xml/formatting.h
#ifndef _aspose_xml_formatting_h_
#define _aspose_xml_formatting_h_

namespace System { namespace Xml {

/// Specifies the formatting options.
enum class Formatting
{
    /// No special formatting.
    None = 0,

    /// Indent child elements according to the System.Xml.XmlTextWriter.Indentation
    /// and System.Xml.XmlTextWriter.IndentChar settings.
    Indented = 1,
};

}} // namespace System::Xml

#endif // _aspose_xml_formatting_h_
