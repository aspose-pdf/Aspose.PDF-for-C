/// @file drawing/drawing2d/pen_type.h
#ifndef _aspose_drawing_drawing2d_pen_type_h_
#define _aspose_drawing_drawing2d_pen_type_h_

namespace System { namespace Drawing { namespace Drawing2D {
/// Specifies a type of a fill Pen object uses.
enum class PenType
{
    /// A solid fill.
    SolidColor = 0,
    /// A hatch fill.
    HatchFill = 1,
    /// A bitmap texture fill.
    TextureFill = 2,
    /// A path gradient fill.
    PathGradient = 3,
    /// A linear gradient fill.
    LinearGradient = 4
};

}}}

#endif
