/// <summary>
///     exif sensing method enum.
/// </summary>
enum class ExifSensingMethod
{
    /// <summary>
    ///     Not defined.
    /// </summary>
    Notdefined = 1,
    /// <summary>
    ///     One chip color area.
    /// </summary>
    OneChipColorArea = 2,
    /// <summary>
    ///     Two chip color area.
    /// </summary>
    TwoChipColorArea = 3,
    /// <summary>
    ///     Three chip color area.
    /// </summary>
    ThreeChipColorArea = 4,
    /// <summary>
    ///     Color Sequential area.
    /// </summary>
    Colorsequentialarea = 5,
    /// <summary>
    ///     Trilinear sensor.
    /// </summary>
    Trilinearsensor = 7,
    /// <summary>
    ///     Color sequential linear sensor.
    /// </summary>
    Colorsequentiallinear = 8
};

