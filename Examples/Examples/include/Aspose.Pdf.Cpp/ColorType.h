namespace Aspose {

namespace Pdf {

/// <summary>
/// Specifies color type of elements on page.
/// </summary>
enum class ColorType
{
    /// <summary>
    /// RGB color type.
    /// </summary>
    Rgb,
    /// <summary>
    /// Grayscale color type.
    /// </summary>
    Grayscale,
    /// <summary>
    /// Black and white color type.
    /// </summary>
    BlackAndWhite,
    /// <summary>
    /// Undefined color type value.
    /// </summary>
    Undefined
};

} // namespace Pdf
} // namespace Aspose


