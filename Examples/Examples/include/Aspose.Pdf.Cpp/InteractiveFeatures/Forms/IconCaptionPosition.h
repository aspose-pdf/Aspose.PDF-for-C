namespace Aspose {

namespace Pdf {

namespace Forms {

/// <summary>
/// Describes position of icon. 
/// </summary>
enum class IconCaptionPosition
{
    /// <summary>
    /// Icon is not displayed.
    /// </summary>
    NoIcon,
    /// <summary>
    /// Caption is not displayed.
    /// </summary>
    NoCaption,
    /// <summary>
    /// Caption is below icon. 
    /// </summary>
    CaptionBelowIcon,
    /// <summary>
    /// Caption is above icon. 
    /// </summary>
    CaptionAboveIcon,
    /// <summary>
    /// Caption to the right.
    /// </summary>
    CaptionToTheRight,
    /// <summary>
    /// Caption to the left.
    /// </summary>
    CaptionToTheLeft,
    /// <summary>
    /// Caption over laid. 
    /// </summary>
    CaptionOverlaid
};

} // namespace Forms
} // namespace Pdf
} // namespace Aspose


