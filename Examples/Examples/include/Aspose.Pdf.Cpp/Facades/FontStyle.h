namespace Aspose {

namespace Pdf {

namespace Facades {

/// <summary>
/// Enumerates 14 types of font.
/// </summary>
enum class FontStyle
{
    /// <summary>
    /// Courier font.
    /// </summary>
    Courier = 0,
    /// <summary>
    /// Bold Courier font.
    /// </summary>
    CourierBold = 1,
    /// <summary>
    /// Oblique Curier font.
    /// </summary>
    CourierOblique = 2,
    /// <summary>
    /// Bold oblique Courier font.
    /// </summary>
    CourierBoldOblique = 3,
    /// <summary>
    /// Helvetica font.
    /// </summary>
    Helvetica = 4,
    /// <summary>
    /// Bold Helvetica font.
    /// </summary>
    HelveticaBold = 5,
    /// <summary>
    /// Oblique Helvetica font.
    /// </summary>
    HelveticaOblique = 6,
    /// <summary>
    /// Bold oblique Helvetica font.
    /// </summary>
    HelveticaBoldOblique = 7,
    /// <summary>
    /// Symbol font.
    /// </summary>
    Symbol = 8,
    /// <summary>
    /// Times-Roman font.
    /// </summary>
    TimesRoman = 9,
    /// <summary>
    /// Bold Times font.
    /// </summary>
    TimesBold = 10,
    /// <summary>
    /// Italic Times font.
    /// </summary>
    TimesItalic = 11,
    /// <summary>
    /// Bold italic Times font.
    /// </summary>
    TimesBoldItalic = 12,
    /// <summary>
    /// ZapfDingbats font.
    /// </summary>
    ZapfDingbats = 13,
    /// <summary>
    /// Unknown font. Standard font will be used.
    /// </summary>
    Unknown = 14,
    /// <summary>
    /// True Type Font for CJK supporting ,default is MS PGothic font.
    /// </summary>
    CjkFont = 15
};

} // namespace Facades
} // namespace Pdf
} // namespace Aspose


