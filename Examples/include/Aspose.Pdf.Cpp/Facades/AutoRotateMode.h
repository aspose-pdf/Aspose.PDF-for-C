namespace Aspose {

namespace Pdf {

namespace Facades {

/// <summary>
/// Direction of the rotation when document is printed.
/// </summary>
enum class AutoRotateMode
{
    /// <summary>
    /// No rotation.
    /// </summary>
    None,
    /// <summary>
    /// Rotation direction is clockwise.
    /// </summary>
    ClockWise,
    /// <summary>
    /// Rotation directon is counterclockwise.
    /// </summary>
    AntiClockWise
};

} // namespace Facades
} // namespace Pdf
} // namespace Aspose


