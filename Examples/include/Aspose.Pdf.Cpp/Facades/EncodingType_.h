namespace Aspose {

namespace Pdf {

namespace Facades {

/// <summary>
/// Enumerates encoding types of the text using.
/// </summary>
enum class EncodingType
{
    /// <summary>
    /// The Unicode encoding with horizontal writing.
    /// </summary>
    Identity_h,
    /// <summary>
    /// The Unicode encoding with vertical writing.
    /// </summary>
    Identity_v,
    /// <summary>
    /// A windows text encoding name.
    /// </summary>
    Cp1250,
    /// <summary>
    /// A windows text encoding name.
    /// </summary>
    Cp1252,
    /// <summary>
    /// A windows text encoding name.
    /// </summary>
    Cp1257,
    /// <summary>
    /// Windows ansi encoding.
    /// </summary>
    Winansi,
    /// <summary>
    /// Mac os standard encoding for Latin text.
    /// </summary>
    Macroman
};

} // namespace Facades
} // namespace Pdf
} // namespace Aspose


