namespace Aspose {

namespace Pdf {

namespace Facades {

/// <summary>
/// Enumeration of possible field flags.
/// </summary>
enum class PropertyFlag
{
    /// <summary>
    /// Field is read-only.
    /// </summary>
    ReadOnly,
    /// <summary>
    /// Field is required.
    /// </summary>
    Required,
    /// <summary>
    /// Field is not exportable.
    /// </summary>
    NoExport,
    /// <summary>
    /// Invalid field flag.
    /// </summary>
    InvalidFlag
};

} // namespace Facades
} // namespace Pdf
} // namespace Aspose

#endif // _Aspose_Pdf_Facades_PropertyFlag_h_

