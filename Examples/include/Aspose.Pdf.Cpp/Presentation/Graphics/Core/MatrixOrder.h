/// <summary>
/// Specifies the order for matrix transform operations.
/// </summary>
enum class MatrixOrder
{
    /// <summary>
    /// The new operation is applied before the old operation.
    /// </summary>
    Prepend,
    /// <summary>
    /// The new operation is applied after the old operation.
    /// </summary>
    Append
};

