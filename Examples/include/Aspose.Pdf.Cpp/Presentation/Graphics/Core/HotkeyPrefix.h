namespace Presentation {

namespace Graphics {

/// <summary>
/// Specifies the type of display for hot-key prefixes that relate to text.
/// </summary>
enum class HotkeyPrefix
{
    /// <summary>
    /// No hot-key prefix.
    /// </summary>
    None,
    /// <summary>
    /// Display the hot-key prefix.
    /// </summary>
    Show,
    /// <summary>
    /// Do not display the hot-key prefix.
    /// </summary>
    Hide
};

} // namespace Graphics
} // namespace Presentation
