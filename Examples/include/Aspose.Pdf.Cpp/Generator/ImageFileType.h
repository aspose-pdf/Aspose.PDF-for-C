namespace Aspose {

namespace Pdf {

/// <summary>
/// Enumerates the image file types.
/// </summary>
enum class ImageFileType
{
    /// <summary>
    /// Unknown type.
    /// </summary>
    Unknown = 0,
    /// <summary>
    /// svg image file type.
    /// </summary>
    Svg = 1,
    /// <summary>
    /// Dicom image file type.
    /// </summary>
    Dicom,
    /// <summary>
    /// Base64 image file type.
    /// </summary>
    Base64
};

} // namespace Pdf
} // namespace Aspose


