/// @file xml/xml_space.h
#ifndef _aspose_xml_xml_space_h_
#define _aspose_xml_xml_space_h_

namespace System { namespace Xml {
/// Specifies xml:space scope.
enum class XmlSpace {
    ///     No xml:space scope.
    None = 0,
    ///     The xml:space scope equals default.
    Default = 1,
    ///     The xml:space scope equals preserve.
    Preserve = 2
};

}}

#endif
