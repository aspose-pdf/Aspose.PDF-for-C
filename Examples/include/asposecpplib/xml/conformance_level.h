#ifndef _aspose_xml_conformance_level_h_
#define _aspose_xml_conformance_level_h_

namespace System { namespace Xml {

/// Specifies how much input/output checking objects of XmlReader and XmlWriter classes perform.
enum class ConformanceLevel {
    /// Auto
    Auto,
    /// Document-level checking.
    Document,
    /// Fragment-level checking
    Fragment
};

}}

#endif // _aspose_xml_conformance_level_h_
