/// @file drawing/drawing2d/coordinate_space.h
#ifndef _aspose_drawing_drawing2d_coordinate_space_h_
#define _aspose_drawing_drawing2d_coordinate_space_h_

namespace System { namespace Drawing { namespace Drawing2D {

/// Specifies how to evaluate coordinates.
enum class CoordinateSpace
{
    /// The coordinates are in the world coordinate context.
    World = 0,
    /// The coordinates are in the page coordinate context.
    Page = 1,
    /// Specifies that coordinates are in the device coordinate context.
    Device = 2
};

}}}

#endif
